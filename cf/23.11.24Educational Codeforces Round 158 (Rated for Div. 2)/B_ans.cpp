#include <iostream>
using namespace std;

void solve() {
    int n;
    cin >> n;
    int lst = 0;//上一位的值
    long long ans = 0;
    for (int i = 0; i < n; ++i) {
        int x;
        cin >> x;
        if (x > lst) {
            ans += x - lst;
        }
        //只要上一位大于此位，可以执行操作1,
        lst = x;
    }
    cout << ans - 1 << "\n";
}

int main() {


    int t;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
