using namespace std;

void solve() {
    int n, x;
    cin >> n >> x;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    for (int i = 0; i < n; ++i) {
        cin >> b[i];
    }
    sort(b.begin(), b.end());
    vector<int> ord(n);
    iota(ord.begin(), ord.end(), 0);
    sort(ord.begin(), ord.end(), [&](int i, int j) { return a[i] < a[j]; });
    vector<int> ans(n);
    for (int i = n - x; i < n; ++i) {
        if (a[ord[i]] <= b[i - (n - x)]) {
            cout << "NO\n";
            return;
        }
        ans[ord[i]] = b[i - (n - x)];
    }
    for (int i = 0; i < n - x; ++i) {
        if (a[ord[i]] > b[i + x]) {
            cout << "NO\n";
            return;
        }
        ans[ord[i]] = b[i + x];
    }
    cout << "YES\n";
    for (auto elem : ans) {
        cout << elem << " ";
    }
    cout << "\n";
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int uwu = 1;
    cin >> uwu;
    while (uwu--) {
        solve();
    }
    return 0;
}
